/* USER CODE BEGIN 4 */
static void UARTPrintf(const char *pFormat,...)
{
	char buffer[64];
	va_list args;
	va_start(args, pFormat);
	__disable_irq();
	int len = vsprintf(buffer, pFormat, args);
	__enable_irq();

	for (int i = 0; i < len; i++)
	{
		__disable_irq();
		HAL_UART_Transmit(&hlpuart1, (uint8_t*) &buffer[i], 1, HAL_MAX_DELAY);
		__enable_irq();
	}
	va_end(args);
}

